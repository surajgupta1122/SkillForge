// Get trending courses (with trending flag or high ratings/students)
export const getTrendingCourses = () => {
  return sampleCourses.filter(course => course.trending).slice(0, 6);
};

// Get featured courses
export const getFeaturedCourses = () => {
  return sampleCourses.filter(course => course.featured).slice(0, 6);
};

// Get all courses
export const getAllCourses = () => sampleCourses;

// Get courses by category
export const getCoursesByCategory = (category) => {
  if (category === 'all') return sampleCourses;
  return sampleCourses.filter(course => course.category === category);
};

// Search courses
export const searchCourses = (query) => {
  return sampleCourses.filter(course => 
    course.title.toLowerCase().includes(query.toLowerCase()) ||
    course.description.toLowerCase().includes(query.toLowerCase()) ||
    course.category.toLowerCase().includes(query.toLowerCase())
  );
};